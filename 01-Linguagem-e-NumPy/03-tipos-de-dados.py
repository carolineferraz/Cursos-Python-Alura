# int
ano = 2023
print(type(ano))

# float
km = 600.2
print(type(km))

# boolean
carro_zero = False
print(type(carro_zero))

# string
nome = "Jetta Variant"
print(type(nome))
print(nome)
nome = "Jetta 'Variant'"
print(nome)
nome = 'Jetta "Variant"'
print(nome)
carro = '''
    Marca
    Modelo
    Ano
    Cor
'''
print(carro)

# none
carro = None
print(type(carro))
