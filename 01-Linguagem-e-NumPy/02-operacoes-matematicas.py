# Adição
x = 2 + 2
print(f'Resultado da Adição: {x}')

# Subtração
x = 6 - 2
print(f'Resultado da Subtração: {x}')

# Multiplicação
x = 2 * 3
print(f'Resultado da Multiplicação: {x}')

# Divisão
x = 10 / 3
print(f'Resultado da Divisão: {x}')

# Divisão inteira
x = 10 // 3
print(f'Resultado da Divisão Inteira: {x}')

# Exponenciação
x = 2 ** 3
print(f'Resultado da Exponenciação: {x}')

# Resto de divisão
x = 10 / 3
print(f'Resultado do Resto da Divisão: {x}')

